    private void ButtonUpdateMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonUpdateMousePressed
        // TODO add your handling code here:
        int kode = Integer.parseInt(TabelKaryawan.getValueAt(TabelKaryawan.getSelectedRow(), 0).toString());
        try {
            // TODO add your handling code here:
            refreshTabel();

            pr = Proyek_PBO.conn.prepareStatement("UPDATE KARYAWAN SET NAMA= '"+tbNama.getText()+"' ,EMAIL='"+tbEmail.getText()+"',ALAMAT='"+tbAlamat.getText()+"',NOMORTELP='"+tbNoTelp.getText()+"', JABATAN= "+cmbPilih.getSelectedIndex()+" where IDKARYAWAN= "+kode+"");
            
            int rowUpdated = pr.executeUpdate();
            if (rowUpdated==1) {
                JOptionPane.showMessageDialog(rootPane, "Berhasil Update Karyawan");
                clear();
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Gagal Update");
            }
            Tampil();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            //Logger.getLogger(MasterStok.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ButtonUpdateMousePressed

    private void cmbJabatanItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbJabatanItemStateChanged
        // TODO add your handling code here:
        int ctr = cmbJabatan.getSelectedIndex();
        String j = cmbJabatan.getItemAt(ctr);
        if (j.equals("MANAGER")) {
            ctr = 0;
        }
        else if (j.equals("ADMIN")) {
            ctr = 1;
        }
        else if (j.equals("KASIR")) {
            ctr = 2;
        }
        if (j.equals("SEMUA")) {
            Tampil();
        }
        else
        {
            try {
                refreshTabel();
                st = Proyek_PBO.conn.createStatement();
                String sql = "select IDKARYAWAN,USERNAME,NAMA,EMAIL,ALAMAT,NOMORTELP,case when JABATAN = 0 Then 'Manager' when JABATAN = 1 then 'Admin' else 'Kasir' end as JABATAN, case when STATUS = 0 Then 'Tidak Aktif' when STATUS= 1 then 'Aktif' end as STATUS from karyawan where jabatan like '%"+ctr+"%' and lower(nama) like '%"+tbCari.getText()+"%'";
                rs = st.executeQuery(sql);
                while (rs.next()) {                
                    Object vdata = new Object[]{rs.getInt("IDKARYAWAN"),rs.getString("USERNAME"),rs.getString("NAMA"),rs.getString("EMAIL"),rs.getString("ALAMAT"),rs.getString("NOMORTELP"),rs.getString("Jabatan"),rs.getString("STATUS")};
                    model.addRow((Object[]) vdata);

    //                model.addRow(rows);
                }

                rs.close();
            } catch (SQLException ex) {
                Logger.getLogger(MasterStok.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_cmbJabatanItemStateChanged

