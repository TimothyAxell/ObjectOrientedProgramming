    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            refreshTabel();
            pr = Proyek_PBO.conn.prepareStatement("INSERT INTO KARYAWAN(USERNAME,PASSWORD,NAMA,EMAIL,ALAMAT,NOMORTELP,JABATAN,STATUS) VALUES(?,?,?,?,?,?,?,?)");
            pr.setString(1,tbUsername.getText());
            pr.setString(2,tbUsername.getText());
            pr.setString(3,tbNama.getText());
            pr.setString(4,tbEmail.getText());
            pr.setString(5,tbAlamat.getText());
            pr.setString(6,tbNoTelp.getText());
            pr.setInt(7,cmbPilih.getSelectedIndex());
            pr.setInt(8,1);
            
            int rowUpdated = pr.executeUpdate();
            if (rowUpdated==1) {
                JOptionPane.showMessageDialog(rootPane, "Berhasil Insert");
                clear();
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Gagal Insert");
            }
            Tampil();
            clear();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            //Logger.getLogger(MasterStok.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

